public class uyg4 {
    // Kendi adını ekrana 5 defa yazdırın. Not: tüm döngülerle
    public static void main(String[] args) {
        // Your code
        // for döngüsü
        for (int i = 0; i < 5; i++) {
            System.out.println("for - Özgür");
        }

        // while döngüsü
        int i = 5;
        while (i > 0) {
            System.out.println("while - Özgür");
            i--;
        }
        i = 5;
        do {
            System.out.println("do_while - Özgür");
            i--;
        } while (i > 0);
    }
}
